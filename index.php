            <script>
              (function() {
                // Конфигурация модуля
                const config = {
                  delay: 4000, // Задержка перед анимацией (10 сек)
                  animationDuration: 2000, // Длительность анимации счетчика
                  staggerDelay: 200 // Задержка между анимациями элементов
                };
                
                // Инициализация после загрузки DOM
                document.addEventListener('DOMContentLoaded', function() {
                  setTimeout(initAchievementsAnimation, config.delay);
                });
          
                function initAchievementsAnimation() {
                  const counters = document.querySelectorAll('.counter-value');
                  const items = document.querySelectorAll('.achievement-item');
                  
                  // Анимация счетчиков
                  counters.forEach(counter => {
                    const target = +counter.dataset.target;
                    const suffix = counter.dataset.suffix || '';
                    animateCounter(counter, target, suffix, config.animationDuration);
                  });
                  
                  // Анимация появления блоков
                  items.forEach((item, index) => {
                    setTimeout(() => {
                      item.classList.add('animated');
                    }, index * config.staggerDelay);
                  });
                }
          
                function animateCounter(element, target, suffix, duration) {
                  const start = 0;
                  const increment = target / (duration / 16);
                  let current = start;
                  
                  const timer = setInterval(() => {
                    current += increment;
                    if (current >= target) {
                      clearInterval(timer);
                      current = target;
                    }
                    element.textContent = Math.floor(current) + suffix;
                  }, 16);
                }
              })();
            </script>
